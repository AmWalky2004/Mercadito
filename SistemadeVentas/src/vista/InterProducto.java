    // ✅ MÉTODO 1: CARGAR CATEGORÍAS EN EL COMBOBOX
    // ============================================================
    private void cargarCategorias() {
        jComboBox_categoria.removeAllItems();
        jComboBox_categoria.addItem("Seleccione categoría...");

        List<Categoria> lista = controlCategoria.listarCategorias();
        if (lista != null) {
            for (Categoria cat : lista) {
                jComboBox_categoria.addItem(cat.getDescripcion());
            }
        }
    }

    // ============================================================
    // ✅ MÉTODO 2: OBTENER ID DE CATEGORÍA SELECCIONADA
    // ============================================================
    private int obtenerIdCategoria(String nombreCategoria) {
        List<Categoria> lista = controlCategoria.listarCategorias();
        if (lista != null) {
            for (Categoria cat : lista) {
                if (cat.getDescripcion().equals(nombreCategoria)) {
                    return cat.getIdCategoria();  // ✅ CORRECTO
                }
            }
        }
        return 0;
    }

    // ============================================================
    // ✅ MÉTODO 3: OBTENER PORCENTAJE DE IVA
    // ============================================================
    private int obtenerPorcentajeIVA(String ivaSeleccionado) {
        if (ivaSeleccionado.equals("12%")) {
            return 12;
        }
        if (ivaSeleccionado.equals("14%")) {
            return 14;
        }
        if (ivaSeleccionado.equals("No grava iva:")) {
            return 0;
        }
        return 0;
    }

