    private void jButton_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_GuardarActionPerformed
        // Validar campos vacíos
        if (txt_nombre.getText().isEmpty() || txt_cantidad.getText().isEmpty()
                || txt_precio.getText().isEmpty() || txt_descripcion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "COMPLETE TODOS LOS CAMPOS");
            return;
        }

        // Validar que cantidad y precio sean números
        try {
            Integer.parseInt(txt_cantidad.getText().trim());
            Double.parseDouble(txt_precio.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Cantidad y Precio deben ser números válidos");
            return;
        }

        // Validar categoría seleccionada
        String categoriaSeleccionada = (String) jComboBox_categoria.getSelectedItem();
        if (categoriaSeleccionada == null || categoriaSeleccionada.equals("Seleccione categoría...")) {
            JOptionPane.showMessageDialog(this, "SELECCIONE UNA CATEGORÍA");
            return;
        }

        // Validar IVA seleccionado
        String ivaSeleccionado = (String) jComboBox_iva.getSelectedItem();
        if (ivaSeleccionado == null || ivaSeleccionado.equals("Seleccione iva:")) {
            JOptionPane.showMessageDialog(this, "SELECCIONE UN IVA");
            return;
        }

        // Crear objeto Producto
        Producto producto = new Producto();
        producto.setNombre(txt_nombre.getText().trim());
        producto.setCantidad(Integer.parseInt(txt_cantidad.getText().trim()));
        producto.setPrecio(Double.parseDouble(txt_precio.getText().trim()));
        producto.setDescripcion(txt_descripcion.getText().trim());
        producto.setPorcentajeIVA(obtenerPorcentajeIVA(ivaSeleccionado));
        producto.setIdCategoria(obtenerIdCategoria(categoriaSeleccionada));
        producto.setEstado(1);

        // Verificar si el producto ya existe
        if (controlProducto.existeProducto(producto.getNombre())) {
            JOptionPane.showMessageDialog(this, "El producto '" + producto.getNombre() + "' ya existe");
            return;
        }

        // Guardar producto
        if (controlProducto.guardar(producto)) {
            JOptionPane.showMessageDialog(this, "¡PRODUCTO REGISTRADO!");
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "¡ERROR AL REGISTRAR EL PRODUCTO!");
        }
    }//GEN-LAST:event_jButton_GuardarActionPerformed


